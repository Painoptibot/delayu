def _catalog_row(code: str) -> dict | None:
    return next((m for m in MODULES_FULL if m["code"] == code), None)


def get_module_hint(module_code: str) -> dict:
    """Подсказка для панели внизу экрана: заголовок, описание, советы."""
    code = (module_code or "").strip()
    if not code:
        return dict(_DEFAULT_HINT)

    row = _catalog_row(code)
    name = row["name"] if row else f"Модуль {code}"
    group = row["group"] if row else ModuleCatalog.Group.CORE

    detail = _MODULE_DETAILS.get(code)
    if not detail:
        group_fb = _GROUP_FALLBACK.get(group, _DEFAULT_FALLBACK)
        detail = {
            "body": group_fb["body"],
            "tips": list(group_fb["tips"]),
        }
        if row:
            detail["body"] = f"{name}. {detail['body']}"

    hint = {
        "title": f"{name} ({code})",
        "body": detail.get("body") or _DEFAULT_FALLBACK["body"],
        "tips": detail.get("tips") or [],
    }

    try:
        db = ModuleCatalog.objects.filter(code=code).values("description", "name").first()
        if db:
            if db.get("name"):
                hint["title"] = f"{db['name']} ({code})"
            if (db.get("description") or "").strip():
                hint["body"] = db["description"].strip()
    except Exception:
        pass

    return hint
